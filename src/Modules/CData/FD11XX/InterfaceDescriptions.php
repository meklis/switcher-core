<?php


namespace SwitcherCore\Modules\CData\FD11XX;


use Exception;
use SwitcherCore\Config\Objects\Oid;
use SwitcherCore\Modules\AbstractModule;
use SwitcherCore\Modules\Helper;
use SwitcherCore\Switcher\Objects\SnmpResponse;
use SwitcherCore\Switcher\Objects\WrappedResponse;

class InterfaceDescriptions extends CDataAbstractModule
{
    /**
     * @var SnmpResponse[]
     */
    protected $response = null;

    function getRaw()
    {
        return $this->response;
    }

    function getPrettyFiltered($filter = [], $fromCache = false)
    {
        return parent::getPrettyFiltered($filter, $fromCache); // TODO: Change the autogenerated stub
    }


    function getPretty()
    {
        $data = [];
        foreach ($this->response as $resp) {
            $onuNum = Helper::getIndexByOid($resp->getOid());
            $ponNum = Helper::getIndexByOid($resp->getOid(), 1);
            $interface = $this->parseInterface(($ponNum * 1000) + $onuNum);
            $data[] = [
                'interface' => $interface,
                'description' => $resp->getValue(),
            ];
        }
        return $data;
    }

    /**
     * @param array $filter
     * @return $this|AbstractModule
     * @throws Exception
     */
    public function run($filter = [])
    {
        if ($filter['interface']) {
            $interface = $this->parseInterface($filter['interface']);
            $data = $this->formatResponse(
                $this->snmp->get([
                    \SnmpWrapper\Oid::init($this->oids->getOidByName('ont.name')->getOid() . ".{$interface['_snmp_id']}"),
                ])
            );
        } else {
            $data = $this->formatResponse(
                $this->snmp->walk(
                    [
                        \SnmpWrapper\Oid::init($this->oids->getOidByName('ont.name')->getOid()),
                    ]
                )
            );
        }
        $resp = $this->getResponseByName('ont.name', $data);
        if($resp->error()) {
            throw new \Exception($resp->error());
        }
        $this->response = $this->getResponseByName('ont.name', $data)->fetchAll();
        return $this;
    }
}

